#operadores matematicos
# Variáveis
a = 6
b = 3

# Adição (+): Soma dois valores
a + b

# Subtração (-): Subtrai um valor de outro
a - b

# Multiplicação (*): Multiplica dois valores
a * b

# Divisão (/): Divide um valor pelo outro
a / b

# Módulo (%): Retorna o restante da divisão de um valor pelo outro
a % b

# Exponenciação (**): Eleva um valor à potência de outro
a ** b

# Divisão inteira (//): Divide dois valores e retorna o resultado inteiro
a // b

#Operadores Lógicos
a = True
b = False

# AND: Retorna True se ambas as expressões forem verdadeiras
a and b

# OR: Retorna True se pelo menos uma das expressões for verdadeira
a or b

# NOT: Inverte o valor booleano da expressão
not a

#Operadores de Comparação
#Variáveis
a = 10
b = 5

# Igual (==): Verifica se dois valores são iguais
a == b

# Diferente (!=): Verifica se dois valores são diferentes
a != b

# Maior que (>): Verifica se um valor é maior que o outro
a > b

# Menor que (<): Verifica se um valor é menor que o outro
a < b

# Maior ou igual (>=): Verifica se um valor é maior ou igual ao outro
a >= b

# Menor ou igual (<=): Verifica se um valor é menor ou igual ao outro
a <= b